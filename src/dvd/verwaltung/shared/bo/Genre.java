package dvd.verwaltung.shared.bo;

public class Genre {

	private int id = 0;
	private String genre = null;

	public void setId(int id) {
		this.id  = id;
	}

	public void setGenre(String genre) {
		this.genre  = genre;		
	}

	public int getId() {
		return this.id;
	}

	public String getGenre() {
		return this.genre;
	}
	
	

}
